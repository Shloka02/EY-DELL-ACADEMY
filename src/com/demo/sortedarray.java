package com.demo;

import java.util.Arrays;

public class sortedarray {
	public static void main(String[] args) {
		int[]  intArr= {10,25,3,45,6};
		Arrays.sort(intArr);
		System.out.println("sorted array "+ Arrays.toString(intArr));
		String[] strArr = {"a","b","h","L"};
		Arrays.sort(strArr);
		System.out.println("sorted arrays"+ Arrays.toString(strArr));
	}
	

}
